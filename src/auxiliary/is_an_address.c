#include "minishell.h"

char	*is_an_address(char *lex)
{
	int	i;

	i = 0;
	while (lex[i] != '\0')
		i++;
	while (lex[i] != '/')
		i--;
	return (ft_strdup(&lex[i + 1]));
}
